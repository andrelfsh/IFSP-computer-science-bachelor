package br.edu.ifsp.sbv.exemplosqlite.modelo;

public class Vilao {
    private Integer id;
    private String nome;
    private Integer perigo; // Nível de perigo




    // Getters e Setters
    public Integer getId() { return id; }
    public void setId(Integer id) { this.id = id; }
    public String getNome() { return nome; }
    public void setNome(String nome) { this.nome = nome; }
    public Integer getPerigo() { return perigo; }
    public void setPerigo(Integer perigo) { this.perigo = perigo; }
}